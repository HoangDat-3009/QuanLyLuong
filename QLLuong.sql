create procedure update_LuongCu
as begin
UPDATE LuongCu
	SET 
		LuongCu.HeSo = HeSo.HeSoLuong,
		PhucapChucVu = ChucVu.PhuCap,
		PhuCapTrinhDo = TrinhDo.PhuCap,
		BHYT = (2000000 * HeSo.HeSoLuong +ChucVu.PhuCap+TrinhDo.PhuCap)* PhanTramBHYT,
		BHTN = (2000000 * HeSo.HeSoLuong +ChucVu.PhuCap+TrinhDo.PhuCap)* PhanTramBHTN,
		BHXH = (2000000 * HeSo.HeSoLuong +ChucVu.PhuCap+TrinhDo.PhuCap)* PhanTramBHXH,
		LuongCu.KhenThuongKyLuat = ISNULL(
            (SELECT SUM(KhenThuongKyLuat.SoTien) 
             FROM KhenThuongKyLuat 
             WHERE KhenThuongKyLuat.MaNhanVien = LuongCu.MaNhanVien
               AND KhenThuongKyLuat.Thang = LuongCu.Thang
               AND KhenThuongKyLuat.Nam = LuongCu.Nam), 0),
		LuongCu.SoCong = CASE 
					WHEN (select COUNT(*) 
					from ChamCong
					where TrangThai != N'Nghỉ' 
						and MaNhanVien = LuongCu.MaNhanVien 
						and MONTH(NgayGioRa) = LuongCu.Thang 
						and YEAR(NgayGioRa) = LuongCu.Nam) = 0 
					THEN 30 
					ELSE (select COUNT(*) 
						  from ChamCong
						  where TrangThai != N'Nghỉ' 
							and MaNhanVien = LuongCu.MaNhanVien 
							and MONTH(NgayGioRa) = LuongCu.Thang 
							and YEAR(NgayGioRa) = LuongCu.Nam) 
				END ,
		LuongCu.ThucLinh = (2000000 * HeSo.HeSoLuong *(CASE 
					WHEN (select COUNT(*) 
					from ChamCong
					where TrangThai != N'Nghỉ' 
						and MaNhanVien = LuongCu.MaNhanVien 
						and MONTH(NgayGioRa) = LuongCu.Thang 
						and YEAR(NgayGioRa) = LuongCu.Nam) = 0 
					THEN 30 
					ELSE (select COUNT(*) 
						  from ChamCong
						  where TrangThai != N'Nghỉ' 
							and MaNhanVien = LuongCu.MaNhanVien 
							and MONTH(NgayGioRa) = LuongCu.Thang 
							and YEAR(NgayGioRa) = LuongCu.Nam) 
				END)/30)
		+ChucVu.PhuCap+TrinhDo.PhuCap -((2000000 * HeSo.HeSoLuong +ChucVu.PhuCap+TrinhDo.PhuCap)*(PhanTramBHTN+PhanTramBHXH+PhanTramBHYT)) +  ISNULL(
            (SELECT SUM(KhenThuongKyLuat.SoTien) 
             FROM KhenThuongKyLuat 
             WHERE KhenThuongKyLuat.MaNhanVien = LuongCu.MaNhanVien
               AND KhenThuongKyLuat.Thang = LuongCu.Thang
               AND KhenThuongKyLuat.Nam = LuongCu.Nam), 0)
		
	FROM LuongCu JOIN NhanVien ON LuongCu.MaNhanVien = NhanVien.MaNhanVien
		JOIN HeSo ON HeSo.MaHeSo = NhanVien.MaHeSo
		JOIN ChucVu ON ChucVu.MaChucVu = NhanVien.MaChucVu
		JOIN TrinhDo ON TrinhDo.MaTrinhDo = NhanVien.MaTrinhDo
		join LuongCoBan_PhanTramBH On MaLuongCoBan_PhanTramBH =1;
end;
