CREATE TABLE PhongBan (
    MaPhongBan INT PRIMARY KEY,
    TenPhongBan NVARCHAR(100),
    DienThoai NVARCHAR(15)
);
CREATE TABLE TrinhDo (
    MaTrinhDo INT PRIMARY KEY,
    TenTrinhDo NVARCHAR(100),
    PhuCap DECIMAL(15,2)
);
CREATE TABLE ChuyenMon (
    MaChuyenMon INT PRIMARY KEY,
    TenChuyenMon NVARCHAR(100)
);
CREATE TABLE ChucVu (
    MaChucVu INT PRIMARY KEY,
    TenChucVu NVARCHAR(100),
    PhuCap DECIMAL(15,2)
);
CREATE TABLE DanToc (
    MaDanToc INT PRIMARY KEY,
    TenDanToc NVARCHAR(50)
);
CREATE TABLE HeSo (
    MaHeSo INT PRIMARY KEY,
    HeSoLuong FLOAT
);
CREATE TABLE LyDoKTKL (
    MaLyDo INT PRIMARY KEY,
    TenLyDo NVARCHAR(255)
);
CREATE TABLE LuongCoBan_PhanTramBH (
	MaLuongCoBan_PhanTramBH INT PRIMARY KEY,
    LuongCoBan DECIMAL(15,2),
	PhanTramBHYT FLOAT,
	PhanTramBHTN FLOAT,
	PhanTramBHXH FLOAT
);

CREATE TABLE NhanVien (
    MaNhanVien INT PRIMARY KEY,
	IsDeleted BIT NOT NULL DEFAULT 0,
    HoTen NVARCHAR(100),
    GioiTinh NVARCHAR(10),
    NgaySinh DATE,
    NoiSinh NVARCHAR(100),
    NgayVaoCongTy DATE,
    MaDanToc INT,
    MaChucVu INT,
    MaPhongBan INT,
    MaTrinhDo INT,
    MaChuyenMon INT,
    DiaChi NVARCHAR(255),
    DienThoai NVARCHAR(15),
    MaHeSo INT,
	CCCD NVARCHAR(20),
    TaiKhoanNganHang NVARCHAR(100),
    SoTaiKhoanNganHang NVARCHAR(50),
    FOREIGN KEY (MaDanToc) REFERENCES DanToc(MaDanToc),
    FOREIGN KEY (MaChucVu) REFERENCES ChucVu(MaChucVu),
    FOREIGN KEY (MaPhongBan) REFERENCES PhongBan(MaPhongBan),
    FOREIGN KEY (MaTrinhDo) REFERENCES TrinhDo(MaTrinhDo),
    FOREIGN KEY (MaChuyenMon) REFERENCES ChuyenMon(MaChuyenMon),
    FOREIGN KEY (MaHeSo) REFERENCES HeSo(MaHeSo)
);

CREATE TABLE ChamCong (
    ChamCongID INT IDENTITY(1,1) PRIMARY KEY,
    MaNhanVien INT,
    NgayGioVao DATETIME,
    NgayGioRa DATETIME,
    TrangThai NVARCHAR(50),
    GhiChu NVARCHAR(255) NULL,
    FOREIGN KEY (MaNhanVien) REFERENCES NhanVien(MaNhanVien)
);

CREATE TABLE KhenThuongKyLuat (
    MaKTKL INT primary key,
    MaNhanVien INT,
    LoaiKTKL NVARCHAR(50),
    Thang INT,
    Nam INT,
    MaLyDo INT,
    SoTien DECIMAL(15,2),
    FOREIGN KEY (MaNhanVien) REFERENCES NhanVien(MaNhanVien),
    FOREIGN KEY (MaLyDo) REFERENCES LyDoKTKL(MaLyDo)
);
CREATE TABLE Luong (
    MaLuong INT IDENTITY(1,1) PRIMARY KEY,
	MaLuongCoBan_PhanTramBH INT,
    MaNhanVien INT,
	MaKTKL int,
    Thang INT,
    Nam INT,
	FOREIGN KEY (MaKTKL) REFERENCES KhenThuongKyLuat(MaKTKL),
    FOREIGN KEY (MaNhanVien) REFERENCES NhanVien(MaNhanVien),
	FOREIGN KEY (MaLuongCoBan_PhanTramBH) REFERENCES LuongCoBan_PhanTramBH(MaLuongCoBan_PhanTramBH)
);



CREATE TABLE Quyen (
	MaQuyen INT PRIMARY KEY,
	TenQuyen NVARCHAR(50)
);

	
CREATE TABLE UserLogin (
	MaNhanVien INT PRIMARY KEY,
    username NVARCHAR(50) ,   
    userpassword NVARCHAR(255)  ,
	MaQuyen INT,
	FOREIGN KEY (MaNhanVien) REFERENCES NhanVien(MaNhanVien),
	FOREIGN KEY (MaQuyen) REFERENCES Quyen(MaQuyen)
);
go
CREATE TABLE LuongCu (
    MaLuongCu INT IDENTITY(1,1) PRIMARY KEY,
    MaNhanVien INT,
    Thang INT,
    Nam INT,
	LuongCoBan  decimal,
	HeSo float,
	PhuCapChucVu decimal,
	PhuCapTrinhDo decimal,
	BHYT decimal,
	BHTN decimal,
	BHXH decimal,
	KhenThuongKyLuat decimal,
	SoCong int,
	ThucLinh decimal,
    FOREIGN KEY (MaNhanVien) REFERENCES NhanVien(MaNhanVien),
);
